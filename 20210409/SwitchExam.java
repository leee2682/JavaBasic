public class SwitchExam {
    public static void main(String[] args) {
        int value = 1;
        
        switch(value) {
            case 1: 
                System.out.println("1");
            case 2:
                System.out.println("2");
            case 3:
                System.out.println("3");
            default:
                System.out.println("그 외 다른 숫자");
        }
    }
}